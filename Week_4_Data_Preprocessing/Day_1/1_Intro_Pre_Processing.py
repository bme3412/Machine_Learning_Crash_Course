### Day 1: Introduction to data preprocessing and its importance

### 1. Understanding the need for data preprocessing in machine learning:
    
    ### A. Data preprocessing is a crucial step in machine learning pipeline that involves transforming raw data into a suitable format for modeling.
    
    ### B. Real-world data often contains noise, missing values, inconsistencies, and irrelevant information that can negatively impact the performance of machine learning models.
    
    ### C. Preprocessing helps in cleaning, transforming, and preparing the data to improve the quality and reliability of the models.

        ### It enables better feature extraction, handles data irregularities, and ensures the data is in a compatible format for the chosen machine learning algorithms.